public class ProgramToCheckWhetherNumberIsEvenOrOdd 
{
    public static void main(String[] args) 
    {
        int A ;
        A = 30;

        if (A%2==0) 
        {
            System.out.println(" A is even number ");   
        } 
        else 
        {
            System.out.println(" A is odd number "); 
        }
    }
}
